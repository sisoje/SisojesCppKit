template <typename DoubleArray>
constexpr auto matrix_begin(DoubleArray& arrOfArr) {
    return &arrOfArr[0][0];
}

template <typename DoubleArray>
constexpr auto matrix_end(DoubleArray& arrOfArr) {
    return &arrOfArr[0][0] + sizeof(arrOfArr) / sizeof(arrOfArr[0][0]);
}

